// scripts/fix-translations.mjs
//
// Fixes a structural bug in src/translations/strings.js where keys ended up
// nested inside termsSections[0] instead of at the language root. Also reports
// which keys are missing in each language compared to English.
//
// USAGE:
//   cd coachly-mobile
//   node scripts/fix-translations.mjs
//
// Writes the fixed file to src/translations/strings.fixed.js.
// Diff the two files before replacing the original.

import { fileURLToPath } from "node:url";
import path from "node:path";
import fs from "node:fs/promises";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const SRC = path.resolve(__dirname, "../src/translations/strings.js");
const OUT = path.resolve(__dirname, "../src/translations/strings.fixed.js");

// Keys that legitimately belong inside a termsSections entry — anything else
// nested in there is a paste bug and must be lifted to the language root.
const TERMS_SECTION_KEYS = new Set(["title", "body"]);

// Keys that legitimately belong inside an aboutSections entry.
const ABOUT_SECTION_KEYS = new Set(["title", "body"]);

async function main() {
  // Dynamic import — strings.js uses ESM `export default`.
  const mod = await import(SRC);
  const strings = mod.default;

  if (!strings || typeof strings !== "object") {
    throw new Error("strings.js did not export a default object");
  }

  const languages = Object.keys(strings);
  console.log(`Found ${languages.length} languages: ${languages.join(", ")}`);
  console.log("");

  const fixed = {};
  const liftedReport = {};

  for (const lang of languages) {
    const block = strings[lang];
    const cleanBlock = { ...block };
    const lifted = [];

    // Walk termsSections (if it exists) and rescue misplaced keys.
    if (Array.isArray(cleanBlock.termsSections)) {
      const cleanSections = cleanBlock.termsSections.map((entry) => {
        if (!entry || typeof entry !== "object") return entry;
        const cleanEntry = {};
        for (const [k, v] of Object.entries(entry)) {
          if (TERMS_SECTION_KEYS.has(k)) {
            cleanEntry[k] = v;
          } else {
            // Lift to language root, but don't clobber existing top-level keys
            if (!(k in cleanBlock)) {
              cleanBlock[k] = v;
            }
            lifted.push(k);
          }
        }
        return cleanEntry;
      });
      cleanBlock.termsSections = cleanSections;
    }

    // Same defensive pass for aboutSections, in case the same bug appears.
    if (Array.isArray(cleanBlock.aboutSections)) {
      const cleanSections = cleanBlock.aboutSections.map((entry) => {
        if (!entry || typeof entry !== "object") return entry;
        const cleanEntry = {};
        for (const [k, v] of Object.entries(entry)) {
          if (ABOUT_SECTION_KEYS.has(k)) {
            cleanEntry[k] = v;
          } else {
            if (!(k in cleanBlock)) {
              cleanBlock[k] = v;
            }
            lifted.push(k);
          }
        }
        return cleanEntry;
      });
      cleanBlock.aboutSections = cleanSections;
    }

    fixed[lang] = cleanBlock;
    if (lifted.length) {
      liftedReport[lang] = [...new Set(lifted)].sort();
    }
  }

  // Report what got lifted
  if (Object.keys(liftedReport).length === 0) {
    console.log("No misplaced keys found in any language.");
  } else {
    console.log("Lifted misplaced keys to language root:");
    for (const [lang, keys] of Object.entries(liftedReport)) {
      console.log(`  ${lang}: ${keys.length} keys`);
      // Print a sample so you can sanity-check
      const sample = keys.slice(0, 5).join(", ");
      console.log(`    e.g. ${sample}${keys.length > 5 ? ", …" : ""}`);
    }
  }
  console.log("");

  // Compare each language to English and report missing keys
  const enKeys = new Set(Object.keys(fixed.en ?? {}));
  if (enKeys.size === 0) {
    console.warn("Warning: 'en' block is empty or missing — skipping coverage report.");
  } else {
    console.log(`English has ${enKeys.size} top-level keys.`);
    console.log("Missing keys per language (compared to English):");
    for (const lang of languages) {
      if (lang === "en") continue;
      const langKeys = new Set(Object.keys(fixed[lang] ?? {}));
      const missing = [...enKeys].filter((k) => !langKeys.has(k));
      if (missing.length === 0) {
        console.log(`  ${lang}: ✓ complete`);
      } else {
        console.log(`  ${lang}: ${missing.length} missing`);
        const sample = missing.slice(0, 8).join(", ");
        console.log(`    e.g. ${sample}${missing.length > 8 ? ", …" : ""}`);
      }
    }
  }
  console.log("");

  // Write the fixed file
  const header =
    "// src/translations/strings.fixed.js\n" +
    "// Auto-generated by scripts/fix-translations.mjs.\n" +
    "// Diff against strings.js before replacing.\n\n";
  const body = "const strings = " + JSON.stringify(fixed, null, 2) + ";\n\n" +
    "export default strings;\n";
  await fs.writeFile(OUT, header + body, "utf8");
  console.log(`Wrote: ${OUT}`);
  console.log("");
  console.log("Next steps:");
  console.log("  1. Diff the two files:");
  console.log("       diff -u src/translations/strings.js src/translations/strings.fixed.js | less");
  console.log("  2. If it looks right:");
  console.log("       mv src/translations/strings.fixed.js src/translations/strings.js");
  console.log("  3. Reload the app and verify Workouts screen translates.");
}

main().catch((err) => {
  console.error("Fix failed:", err);
  process.exit(1);
});
