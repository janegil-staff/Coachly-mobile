// fix-translations.mjs
// Run from anywhere — it auto-detects strings.js by walking the tree.
//
// USAGE:
//   cd <anywhere in the coachly-mobile project>
//   node fix-translations.mjs
//
// Writes strings.fixed.js next to the original strings.js.
// Diff and replace manually when satisfied.

import { fileURLToPath } from "node:url";
import path from "node:path";
import fs from "node:fs/promises";

const TERMS_SECTION_KEYS = new Set(["title", "body"]);
const ABOUT_SECTION_KEYS = new Set(["title", "body"]);

async function findStringsFile(startDir) {
  // Walk up from startDir looking for src/translations/strings.js
  let dir = startDir;
  for (let i = 0; i < 6; i++) {
    const candidate = path.join(dir, "src", "translations", "strings.js");
    try {
      await fs.access(candidate);
      return candidate;
    } catch {
      // try parent
    }
    const parent = path.dirname(dir);
    if (parent === dir) break;
    dir = parent;
  }
  throw new Error(
    "Could not find src/translations/strings.js anywhere up the tree from " +
    startDir
  );
}

async function main() {
  const cwd = process.cwd();
  const SRC = await findStringsFile(cwd);
  const OUT = SRC.replace(/strings\.js$/, "strings.fixed.js");

  console.log(`Source:  ${SRC}`);
  console.log(`Output:  ${OUT}`);
  console.log("");

  const mod = await import(SRC);
  const strings = mod.default;

  if (!strings || typeof strings !== "object") {
    throw new Error("strings.js did not export a default object");
  }

  const languages = Object.keys(strings);
  console.log(`Languages: ${languages.join(", ")}`);
  console.log("");

  const fixed = {};
  const liftedReport = {};

  for (const lang of languages) {
    const block = strings[lang];
    const cleanBlock = { ...block };
    const lifted = [];

    if (Array.isArray(cleanBlock.termsSections)) {
      cleanBlock.termsSections = cleanBlock.termsSections.map((entry) => {
        if (!entry || typeof entry !== "object") return entry;
        const cleanEntry = {};
        for (const [k, v] of Object.entries(entry)) {
          if (TERMS_SECTION_KEYS.has(k)) {
            cleanEntry[k] = v;
          } else {
            if (!(k in cleanBlock) || cleanBlock[k] === entry[k]) {
              cleanBlock[k] = v;
            }
            lifted.push(k);
          }
        }
        return cleanEntry;
      });
    }

    if (Array.isArray(cleanBlock.aboutSections)) {
      cleanBlock.aboutSections = cleanBlock.aboutSections.map((entry) => {
        if (!entry || typeof entry !== "object") return entry;
        const cleanEntry = {};
        for (const [k, v] of Object.entries(entry)) {
          if (ABOUT_SECTION_KEYS.has(k)) {
            cleanEntry[k] = v;
          } else {
            if (!(k in cleanBlock)) cleanBlock[k] = v;
            lifted.push(k);
          }
        }
        return cleanEntry;
      });
    }

    fixed[lang] = cleanBlock;
    if (lifted.length) liftedReport[lang] = [...new Set(lifted)].sort();
  }

  if (Object.keys(liftedReport).length === 0) {
    console.log("No misplaced keys found — file is already structurally clean.");
  } else {
    console.log("Lifted misplaced keys to language root:");
    for (const [lang, keys] of Object.entries(liftedReport)) {
      console.log(`  ${lang}: ${keys.length} keys`);
    }
  }
  console.log("");

  // Coverage report
  const enKeys = new Set(Object.keys(fixed.en ?? {}));
  console.log(`English has ${enKeys.size} top-level keys.`);
  console.log("Missing keys per language:");
  for (const lang of languages) {
    if (lang === "en") continue;
    const langKeys = new Set(Object.keys(fixed[lang] ?? {}));
    const missing = [...enKeys].filter((k) => !langKeys.has(k));
    if (missing.length === 0) {
      console.log(`  ${lang}: complete`);
    } else {
      console.log(`  ${lang}: ${missing.length} missing`);
    }
  }
  console.log("");

  const header =
    "// src/translations/strings.fixed.js\n" +
    "// Auto-generated by fix-translations.mjs.\n" +
    "// Diff against strings.js, then mv to replace.\n\n";
  const body =
    "const strings = " + JSON.stringify(fixed, null, 2) + ";\n\n" +
    "export default strings;\n";
  await fs.writeFile(OUT, header + body, "utf8");

  console.log(`Wrote ${OUT}`);
  console.log("");
  console.log("Next:");
  console.log(`  diff -u ${SRC} ${OUT} | less`);
  console.log(`  mv ${OUT} ${SRC}`);
  console.log("  npx expo start -c");
}

main().catch((err) => {
  console.error("Fix failed:", err.message);
  process.exit(1);
});
